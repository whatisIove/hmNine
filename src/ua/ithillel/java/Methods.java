package ua.ithillel.java;

public class Methods {

    public static double add(double a, int b) {
        double sum = a + b;
        return sum;
    }

    public static double add(double x, double y) {
        double sum = x + y;
        return sum;
    }

    public static double add(byte val, double value) {
        double sum = val + value;
        return sum;
    }

    public static double add(int a, double b, int c) {
        double sum = a + b + c;
        return sum;
    }

    public static double sub(double a, double b) {
        double sub = a - b;
        return sub;
    }

    public static double mul(int a, double b) {
        double mul = a * b;
        return mul;
    }

    public static int div(int a, int b) {
        int div = a / b;
        return div;
    }
}
